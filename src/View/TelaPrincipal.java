    public void LoadSearchProduct(String busca, String context) {

        ArrayList<String[]> linhasProdutos = ProdutoController.getProdutos(busca);

        DefaultTableModel tmProdutos = new DefaultTableModel();
        tmProdutos.addColumn("Cod. Produto");
        tmProdutos.addColumn("Categ.");
        tmProdutos.addColumn("Nome Produto");
        tmProdutos.addColumn("Desc. Produto");
        tmProdutos.addColumn("Qntd.");
        tmProdutos.addColumn("Vlr. Unit. R$");

        for (String[] c : linhasProdutos) {
            tmProdutos.addRow(c);
        }
        if (context.equals("cadastroVenda")) {
            tblProdutos.setModel(tmProdutos);
        } else if (context.equals("cadastroProduto")) {
            tblProdutosCadastrados.setModel(tmProdutos);
        }
    }

    public void LoadReport(String from, String to) {

        ArrayList<String[]> linhasProdutos = ItemPedidoController.getReports(from, to);
        
              
        DefaultPieDataset dataset = new DefaultPieDataset();


        DefaultTableModel tmProdutos = new DefaultTableModel();
        tmProdutos.addColumn("Produto");
        tmProdutos.addColumn("Categoria");
        tmProdutos.addColumn("Quantidade");
        tmProdutos.addColumn("Valor Total");
        
        double valortotal = 0;
        for (String[] c : linhasProdutos) {
            dataset.setValue(c[0] + " - " + c[2], new Double(c[2]));
            valortotal = valortotal + new Double(c[3]);
            tmProdutos.addRow(c);
        }
        
        lblValorRelatorio.setText(Double.toString(valortotal));
        
        JFreeChart chart = ChartFactory.createPieChart(
                "Grafico de Produtos Vendidos", // chart title
                dataset, // data
                true, // include legend
                true,
                false);
        
        int width = 640;
        /* Width of the image */
        int height = 480;
        /* Height of the image */
        File pieChart = new File("src/View/imgs/PieChart.jpeg");
        try {
            ChartUtilities.saveChartAsJPEG(pieChart, chart, width, height);
            //pieChart.setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        BufferedImage img = null;
        try {
            img = ImageIO.read(new File("src/View/imgs/PieChart.jpeg"));
        } catch (IOException e) {
            
        }

        this.pieChart.setIcon(new javax.swing.ImageIcon(img));
        this.pieChart.setVisible(true);

        tblResultados.setModel(tmProdutos);

    }

